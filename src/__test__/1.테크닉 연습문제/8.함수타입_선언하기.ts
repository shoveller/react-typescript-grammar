export {};

/**
 * 아래의 비동기작업 이라는 함수는 콜백함수를 매개변수로 받습니다
 * 1초 후에 콜백함수를 실행하고, 콜백함수의 반환값이 true 이면 메시지를 출력합니다.
 * 콜백함수는 숫자 타입의 매개변수를 받습니다.
 * 아래의 문서를 참조해서 콜백함수타입을 완성해 보세요.
 * 가능하다면 콜백함수인터페이스도 완성해 보세요.
 * @link https://www.typescriptlang.org/ko/docs/handbook/2/functions.html
 */
type 콜백함수타입 = (값: number) => boolean;

interface 콜백함수인터페이스 {
  (값: number): boolean;
}

/**
 * 1. 자동완성 기능으로 인터페이스 생성
 * 2. 불필요한 타입 덜어내기
 * 3. 인라인 어노테이션 잘라내서 인터페이스로 만들기
 */
const 비동기작업 = (콜백함수: 콜백함수인터페이스) => {
  setTimeout(() => {
    const 실행완료 = 콜백함수(2);
    if (실행완료) {
      alert("완료되었습니다.");
    }
  }, 1000);
};

비동기작업((값: number) => {
  return 값 === 1;
});


const 비동기작업2 = (콜백함수) => {
  // 2. setTimeout 함수는 1초 뒤에 첫번째 매개변수로 받은 함수를 실행
  setTimeout(() => {
    // 3. 1초 후에 콜백함수 실행
    const 실행완료 = 콜백함수(1);
    if (실행완료) {
      alert("완료되었습니다.");
    }
  }, 1000);
};

// 1. 여기서부터 시작, 콜백함수는 나중에 실행됨
비동기작업2((값) => {
  // 4. 값이 1이면 true, 아니면 false
  return 값 === 1;
});